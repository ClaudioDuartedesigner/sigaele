	<h2>O desabafo de Deus</h2>
    <p>O filho honra seu pai, e o servo o seu senhor. Se eu sou pai, onde está a honra que me é devida? Se eu sou senhor, onde está o temor que me devem? ", pergunta o Senhor dos Exércitos a vocês, sacerdotes. "São vocês que desprezam o meu nome!" Malaquias 1:6</p>

    <p>Muitas pessoas dizem “Sou crente”. Existem muitas igrejas evangélicas espalhadas por aí, mas será que todos os que estão dentro das igrejas estão agradando ou tentando agradar a Deus? Precisamos refletir sobre isso à luz da Palavra de Deus e aprender como agradá-lo. Através do Profeta Malaquias Deus resolve questionar seu povo, vamos ver o que Deus quer falar com o povo?</p>

    <h2>Deus está triste e decepcionado com o povo</h2>

	<p>  Uma das frases mais importantes ditas no capítulo 1 versículo 2 é “Eu sempre os amei”, mas o povo questionava o amor de Deus. Fico pensando, como ficou o sentimento de Deus em relação ao homem? Ele criou o homem, fez um plano para seu povo e o povo respondeu ao Senhor com falsas adorações. No versículo 6 Deus questionou o povo reivindicando a sua autoridade de pai e Senhor, Ele questiona pelo fato do mesmo não dar nenhuma importância ou ser indiferente a oferta que trazia ao altar, por costume as pessoas faziam seus cerimoniais e davam ofertas ao Senhor, porém de qualquer maneira. Como tem sido nossas ofertas? Como temos prestado o nosso culto? Será que caímos no costume de ir a igreja e esquecemos do real sentindo de estar lá? Deus falou que não suportava mas os cultos, no versículo 10 diz que seria melhor se alguém tivesse a coragem de fechar a porta do templo, para não ter mais que suportar os cultos sem importância da parte do homem, sem reverência e respeito. O povo estava tão distante de Deus que oferecia o pior dos seus animais, aquilo que não servia mais para nada, era esse que o povo levava ao Senhor. Devemos ter cuidado com o que oferecemos ao Senhor, saber chegar a presença Dele. A primeira oferta que devemos apresentar é um coração puro de adorador arrependido buscando sua graça.</p>

	<h2>Deus está decepcionado com aos sacerdotes</h2>

    <p>Uma das coisas terríveis que Deus falou com seus sacerdotes está registrada no versículo 3 do capítulo 2, Deus diz que iria esfregar a cara deles nos excrementos dos animais. O grande problema dos sacerdotes foi a quebra da aliança, eles eram os responsáveis pelo desvio de muita gente, ao invés de aconselhar ao povo se voltar para Deus, eles afastaram o povo de Deus, por isso o Senhor os responsabiliza, por não seguir os caminhos de Deus seriam humilhados.</p>

    <p>  Será que hoje, pastores, líderes ou aqueles que ensinam realmente estão comprometidos com a Palavra de Deus? O que se vê é muita distorção em relação aos ensinos, estão mais preocupados com igrejas cheias e prosperidade do que a pregação da verdade, do que o compromisso sério com Deus, por isso é conivente a práticas erradas, só pregam bênçãos e esquece de pregar a palavra que transforma. Os cultos estão mais direcionados a pessoas do que ao próprio Deus.</p>

	<h2>Deus fala a Judá</h2>
    <p>As pessoas choram no altar, derramam lágrimas, sabem que o Senhor não aceita as ofertas, e ainda perguntam, por que? Sabem que estão no erro e que não estão vivendo conforme a Lei, mas se fazem de desentendidas. Havia adultério no meio do povo, as pessoas eram infiéis umas para com as outras.  A palavra do Senhor para eles é: “Estou cansado das vossas palavras”. O povo questiona a justiça de Deus, compara a vida do ímpio com a vida do povo de Deus e acham que o ímpio vive melhor, eles deixaram de entender o espiritual e passaram a viver o material, reclamavam de tudo, não davam mais os dízimos, roubavam a Deus.</p>

    <p>O que causa tristeza em Deus é a falta de confiança, quando duvidamos de Deus e do que Ele está fazendo em nossa vida estamos magoando diretamente a Deus, pois não acreditamos em seus cuidados.</p>

	<h2>Deus promete um futuro melhor</h2>

    <p>Deus faz promessas de que tudo seria restaurado, o povo seria provado como o ouro e provado no fogo, só depois disso que haveria uma verdadeira consciência de adoração, todos veriam a diferença entre um e outro, entre o que servia a Deus e o que não servia. No dia em que o Senhor agisse o povo que buscasse a Deus seria tesouro particular do Senhor.</p>

    <p>  É interessante hoje pensarmos nessa questão: Como eu tenho vivido diante de Deus? Será que eu tenho me preparado para encontrar com Ele ou ainda tenho o meu pensamento todo voltado para minha vida secular? Viver, projetar coisas, estudar e trabalhar para um futuro melhor não tem problema nenhum. Isso é o que devemos fazer, de fato não podemos relaxar com essas coisas, são importantes para a nossa vida, porém temos que prestar atenção no que se diz respeito às prioridades, será que Deus é prioridade pra nós? Precisamos nos questionar sobre isso, pois muitas vezes não é, colocamos tudo na frente do Senhor até mesmo as coisas da igreja, não perguntamos a Ele como deve ser, questionamos o tempo todo a Deus sobre as coisas que acontecem, mas não queremos parar para ouvir sua voz, e tentar entender seus planos.</p>

    <p>Deus nos fez promessas maravilhosas, Ele nos abençoa todos os dias e tem cuidado de nós. Que possamos estar ligados com Ele e não viver mais na superficialidade, mas chegar-se a Deus com um coração contrito e quebrantado, buscando o arrependimento e o amor de Deus, que possamos olhar assim para Deus.</p>

    <h2>Conclusão</h2>

	<p>Nossa vida é preciosa diante de Deus, não podemos cair no costume de ir para igreja diariamente e encarar as coisas como rotina, mas temos que olhar cada culto como uma grande celebração e uma oportunidade de salvação, cura e conserto com o Senhor. Não podemos perder a reverência ao nosso Deus e cair no erro de pensar que Deus não se importa com isso.</p>

